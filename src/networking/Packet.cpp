#include "Packet.hpp"

Packet::Packet()
	: bytes(1400)
	, bytes_written(0)
	, bytes_read(0)
{
}

Packet& Packet::operator <<(std::string data)
{
	*this << data.length();
	serialize(data.data(), data.length());
	return *this;
}

Packet& Packet::operator >>(std::string& data)
{
	std::size_t length;
	*this >> length;
	data.resize(length);
	deserialize(data.data(), length);
	return *this;
}
